
//Soma
function soma (a, b) {
    return a + b
}
console.log(soma(10, 5))

//Subtracao
function subtracao (a, b) {
    return a - b
}
console.log(subtracao(10, 5))

//Multiplicacao

function multiplicacao (a, b) {
    return a * b
}
console.log(multiplicacao(10, 5))

// Divisao
function divisao (a, b) {
    return a % b
}
console.log(divisao(10, 5))


